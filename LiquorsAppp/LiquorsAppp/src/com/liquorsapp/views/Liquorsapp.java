    private void deleteSelectedRow() {
    int selectedRow = tblDisplayDrinksAdminPage.getSelectedRow();
    if (selectedRow != -1) { 
        DefaultTableModel deleterow1 = (DefaultTableModel) tblDisplayDrinksAdminPage.getModel();
        DefaultTableModel deleterow2 = (DefaultTableModel) tblDisplayDrinks.getModel();
        deleterow1.removeRow(selectedRow); 
        deleterow2.removeRow(selectedRow);
    } else {
        JOptionPane.showMessageDialog(pnlAdminPage, "Please select a row to delete.","Failed", JOptionPane.INFORMATION_MESSAGE); 
    }
}
    
