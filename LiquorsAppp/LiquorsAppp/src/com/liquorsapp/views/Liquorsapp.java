    private void btnOrderUserPageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderUserPageActionPerformed
        // TODO add your handling code here:
        int id=Integer.parseInt(txtFldDrinkIdUserPage.getText());
        int quantity=Integer.parseInt(txtFldQuantityUserPage.getText());
        String cname= txtFldCustomerNameUserPage.getText();
        String dname= txtFldDrinkNameUserPage.getText();
        
        try{
            if (txtFldCustomerNameUserPage.getText().isEmpty() || txtFldDrinkIdUserPage.getText().isEmpty() || txtFldDrinkNameUserPage.getText().isEmpty() || 
                 txtFldQuantityUserPage.getText().isEmpty()) {
            JOptionPane.showMessageDialog(pnlLogin, "Please fill out all the fields","Failed", JOptionPane.WARNING_MESSAGE);
            }
            else{
                if(id<0 || id>100){
                    JOptionPane.showMessageDialog(pnlUserPage, "Order not placed. Drink ID must be between 0 and 100","Failed", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                
                if(quantity<0 || quantity>100){
                    JOptionPane.showMessageDialog(pnlUserPage, "Order not placed. Quantity must be between 0 and 100","Failed", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            DefaultTableModel fororder = (DefaultTableModel) tblUserDrinksAdminPage.getModel();
            fororder.addRow(new Object[]{cname, id, dname, quantity});

       
            txtFldDrinkIdUserPage.setText("");
            txtFldQuantityUserPage.setText("");
            txtFldCustomerNameUserPage.setText("");
            txtFldDrinkNameUserPage.setText(""); 
            JOptionPane.showMessageDialog(pnlUserPage, "Order successfully placed.", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
            
        }
        catch(NumberFormatException lecturererror){
                    JOptionPane.showMessageDialog(pnlUserPage, "Order not placed. Please input valid data", "Failed", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnOrderUserPageActionPerformed

