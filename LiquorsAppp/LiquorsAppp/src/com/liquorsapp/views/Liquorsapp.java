    private void btnCreateAdminPageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateAdminPageActionPerformed
        // TODO add your handling code here:
        int drinkId=Integer.parseInt(txtFldDrinkIDAdminPage.getText());
        String drinkName= txtFldDrinkNameAdminPage.getText();
        int price=Integer.parseInt(txtFldPriceAdminPage.getText());
        String flavor= txtFldFlavorAdminPage.getText();
        int calorie=Integer.parseInt(txtFldCalorieAdminPage.getText());

        try{
            if (txtFldDrinkIDAdminPage.getText().isEmpty() || txtFldDrinkNameAdminPage.getText().isEmpty() || txtFldPriceAdminPage.getText().isEmpty() || 
                 txtFldPriceAdminPage.getText().isEmpty() || txtFldCalorieAdminPage.getText().isEmpty()) {
                 JOptionPane.showMessageDialog(pnlLogin, "Please fill out all the fields","Failed", JOptionPane.WARNING_MESSAGE);
                 }
            
            else{
                if(drinkId<0 || drinkId>100){
                    JOptionPane.showMessageDialog(pnlUserPage, "Item not added. Drink ID must be between 0 and 100","Failed", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                
                if(price<0){
                    JOptionPane.showMessageDialog(pnlUserPage, "Item not added. Price cannot be negative","Failed", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                
                if(calorie<0 || calorie>100){
                    JOptionPane.showMessageDialog(pnlUserPage, "Item not added. Calorie must be between 0 and 100","Failed", JOptionPane.WARNING_MESSAGE);
                    return;
                }    
                }
            
            
            }
        
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(pnlUserPage, "Item not added. Please input valid data", "Failed", JOptionPane.WARNING_MESSAGE);
        }

        AdminModel drink = new AdminModel( drinkId,  drinkName,  price,  flavor,  calorie);
        addDrinks(drink);
        txtFldDrinkIDAdminPage.setText("");
        txtFldDrinkNameAdminPage.setText("");
        txtFldPriceAdminPage.setText("");
        txtFldFlavorAdminPage.setText("");
        txtFldCalorieAdminPage.setText("");

            
            

        
    }//GEN-LAST:event_btnCreateAdminPageActionPerformed

